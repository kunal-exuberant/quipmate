<div class="help-title">Introduction</div>
Quipmate is a social network which is based on a person's social engagements. It has a number of innovative features which helps a person engage with people/friends who matter them in their real life.
</div>
<div class="feature">
<div class="help-title"> Quipmate Features</div>
Following is a list of features that are present at Quipmate. Please note that with time we are modifying the existing features of Quipmate making them more user-friendly with the help of feed-back from our members. We have been and will always keep introducing new features as and when we feel there is appropriate time for that.
</div>
<div class="feature">
<div class="help-title"> Diary</div>  
We create the online diary of the members of Quipmate. This concept is simulations of the diaries that people are used to write. They maintain all of their activities and experiences. Thus in this era of the Internet, we provide them online diary structure to do the same and provide a lot extra facilities to manage them. In the dairy, all the activity of members is recorded and the owner of diary can control the visibility and privacy of the post from the friends. There are two types of the diary we have implemented. First one is My Diary is the default diary of the members in which all the activities of the member is recorded along with the friends activity on that member. Second is the Interest Diary that is the diary any one can create on the basis of their interest/hobby. The members of the Quipmate can be the fan of diary on the basis of that diary privacy. Interest Diary privacy is Open Diary/Close Diary.
</div>
<div class="feature">
<div class="help-title"> Status Song</div>
We are trying to do our best to put the feeling of person online. Music is the best way to express the feeling. With this whole idea, we have launched the feature named Status Song. In this feature, there is an interface using which we can search the songs and put it as a status. Friends can play these songs and listen to them. We also know that there are a lot of songs which describe the same as your feelings, thus it will be easy to share our feeling via songs.  
</div>
<div class="feature">
<div class="help-title"> Song Dedication</div>
Along with Status Song, we can also dedicate a song to our friends. Unlike Status Song, which expresses what you are feeling, Song Dedication expresses what you are feeling for your friends i.e. you can sing a song for your friends.  
</div>
<div class="feature">
<div class="help-title"> Miss-U  </div>
This is another exciting feature that we have implemented to say your friend that you are missing them. They can miss you back in response to your miss you. To maintain the privacy, we don't reveal the identity of the person who is missing their friends.
</div>
<div class="feature">
<div class="help-title">Crush-Match </div>
On moving forward to simulate the online social structure, we can't avoid to explore the relationship. Basically this feature is for lovers. Crush match is another very unique feature created for people who cannot express their love to their crush. Using crush match they can add their love to their crush list. In that case his/her crush receives an anonymous email from Quipmate telling someone added her/him into crush list. Crush match happens when his/her crush adds back that person into his/her crush list. Meanwhile using personal messaging both of them can talk keeping the initiators identity hidden. To add your crush to crush list, enter your crush's email in the provide box. After you add your crush into your crush list, your crush will receive an anonymous email from Quipmate saying someone added her/him as crush at Quipmate. Person's identity will not be revealed in this email. This email will also contain a link which your crush will use to communicate with you using Quipmate's personal messaging system. You will find your crush's messages inside your inbox at Quipmate. You can use this thread to communicate with your crush. Meanwhile if your crush gets some hint about you, she/he will attempt adding you as crush at Quipmate. If she/he also adds you as crush at Quipmate that means she/he too has crush on you. It's a crush match. Now we reveal your identity now. Yours as well as your crush's friend will get an update about successful crush match.
</div>
<div class="feature">
<div class="help-title">Pin Board </div>
	It is said that a picture is worth of thousand words. Any SNS will look dead if it doesn't provide the image sharing. Pin Board is a photo sharing tool that displays the photos of people collectively that is shared with the member. It is another exciting way of representing the collection of photos. This tool also shows the comments made on those pictures and can also put comments on that. Members can also view the image of member individually by visiting their profiles.
	</div>
<div class="feature">
<div class="help-title">Mood Sharing </div>
Mood sharing is the pictorial way of representing the way a member feels at particular time. In mood sharing, there's a list of emoticons that is used to represent the various expressions of human. Friends can also response to these shared posts.
</div>
<div class="feature">
<div class="help-title">Tagline</div>
	Tagline is a short message that describes what drives a member for a particular span of time. It displays along with the each post of the members. Members can set their tagline by visiting their profiles. 
</div>
<div class="feature">
<div class="help-title">Gift </div>
This is also a very exciting feature that simulates the trend of sending gifts to friends in a particular occasion. There is a set of gift i.e. is a virtual gifts or pictorial gifts members can send to their friends. This sharing is open i.e. this is visible to friends of sender as well as receiver.
</div>
<div class="feature">
<div class="help-title">Exciting</div>
Exciting button is used to express a feeling of exciting on a member's post,photo or other actions.
</div>
<div class="feature">
<div class="help-title">Sad</div>
Sad button is used to express a feeling of sadness on a member's post,photo or other actions. Sad does not mean dislike. Sad response is more likely to come from a member when his/her friend sees a sad action by owner of the post.
</div>
<div class="feature">
<div class="help-title">New-Pinch</div>
New-Pinch is associated with the profile picture change. Whenever any member changes his/her profile picture, friends of that member can respond to this action by new-pinching him/her because changing profile picture is analogous to wearing a new dress in the real life. New-Pinch is common among friends to give complements on the appearance. 	
</div>
<div class="feature">
<div class="help-title">Single Player Games</div>
Single player game can be played from a link available at the chat-bar, fourth from the left.
Just click to select a game of choice and play it.
</div>
<div class="feature">
<div class="help-title">Two Player Games</div>
Two player game can be played with an online friend. When a friend is online whom with you want to play a chat game just open the chat window of that particular person and click on the icon next to smily to get the list of two player games. Then just select any game out of the list. When you select a game we will send a game request to your friend who upon accepting can play that game with you.
</div>
<div class="feature">
<div class="help-title">Chat</div>
Online Chat is also part of Quipmate. Chat can be used to interact with the online friends of a particular member. Only a friend of a member appears in the chat. 
</div>
<div class="feature">
<div class="help-title">Chat Room</div>
Chat Rooms is also incorporated in which members can create their own chat room and invite their friends to the room. Thus this provides the facility of chatting with more than one person. There are various applications in this SNS. The Friend Finder Tool let you search your friend in Quipmate world. If members have made mistakes then they can also delete their post, images. Search tool is also there to assist member to find the friends and Interests Diaries, school and colleges. We provide an extensive privacy on the information provided by the members of the Quipmate so that they can control their personal data.
