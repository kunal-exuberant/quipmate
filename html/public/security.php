<h1>Quipmate Security Policy</h1>

<p><i>Version 1.1 Effective April 1, 2014</i></p>

<p>Thank you for visiting our website. We are committed to protecting our visitors&#39 and members&#39 security through holistic approach. The following Security Policy outlines the approach Quipmate Enterprise Solutions Private Limited, (Quipmate or the Company) use for security of members data.</p>

<p>BY USING QUIPMATE, YOU ARE ACCEPTING THE PRACTICES DESCRIBED IN THIS SECURITY POLICY.</p>

<p>To prevent unauthorized access or disclosure, maintain data accuracy, and ensure the appropriate use of personal information, Quipmate has adopted appropriate physical, electronic and managerial procedures to safeguard and secure the personal information we process. We follow generally accepted industry standards to protect the personal information submitted to us, both during transmission and once we receive it. Below is list of areas that we cover to tackle all possible facets needed to provide a secured:</p>
<ul>
<li>Data Security</li>
<li>Network Security</li>
<li>Physical Security</li>
<li>Product Development Life Cycle</li>
<li>Administrative Policies</li>
</ul>
<h2>Data Security</h2>
<p>Data maintained by Quipmate is protected by computer security mechanisms including firewalls, encryption, monitoring software, data and network segmentation, and passwords. We are proudly associated with <a href="http://www.rackspace.com/">Rackspace</a>, who are among the best in the world to provide cloud hosting solutions and security.</p>

<p>The security of your personal information is important to us. When you enter sensitive information (such as a credit card number) on our order forms, we encrypt the transmission of that information using secure socket layer technology (SSL). To boost security, we are using Organization Validated SSL certified from Comodo.</p>

<p>For more details, please visit <a href="http://www.rackspace.com/security">http://www.rackspace.com/security</a></p>

<p>For Comodo SSL certification, please visit <a href="https://ssl.comodo.com/">https://ssl.comodo.com</a></p>

<h2>Network Security</h2>

<p>Administrator access for web is restricted from company owned static IPs through a firewall, and from allowed IPs, access is only possible if user has a key file. There is a firewall for each of the connections to the internet or other untrusted networks. </p>

<p>Quipmate Production servers cannot be accessed from anywhere except from within the company&#39s secure internal network (with access strictly limited to only those with relevant job responsibility).</p>

<p>We have kept our machines secure by:</p>
<ul>
<li>Anti-virus products are required for all systems regardless of the purpose of the machine as long as it on the network</li>

<li>All machines are kept up to date with latest security patches and updates from relevant vendors</li>
</ul>
<p>Audit logs for system activity - like unsuccessful login attempts and network problems - are reviewed frequently.</p>

<h2>Physical Security</h2>

<p>Company facility has proper security measures in place to restrict any form of unauthorized physical access of the premises</p>
<ul>
<li>Employees sign Non-Disclosure agreement (NDA) after joining the company</li>

<li>Employees are required to keep wearing their identification badges in premises</li>

<li>Employees are required to authenticate themselves before accessing any machine, network resource or source code </li>

<li>We don&#39t allow visitors to enter in our computer labs</li>
</ul>
<p>If, during their employment, Quipmate employees acquire confidential or proprietary information belonging to Quipmate or its customers, such information is required to be handled in confidence and may not be disclosed to unauthorized third parties. Quipmate employees are also responsible for the internal security of such information. Quipmate provides information and training to all employees who have access to data maintained on behalf of Quipmate customers. Employees who violate Quipmate security and safe maintenance of data policies are subject to appropriate discipline including, but not limited to, termination</p>

<h2>Product Development Life Cycle</h2>

<p>All software code is developed based on strict and well defined industry standard Agile principles. </p>

<p>Each feature or change goes through at least the following stages - Product Definition followed by review and sign off, Engineering Development, Code Complete (CC), Quality Assurance (for testing), Zero Bug Build (ZBB) and staging to Release To World (RTW).</p>

<p>No code is ever released without proper testing (including security testing). Tests include but are not limited to &mdash; Cross site scripting (XSS), Invalid user input, authentication issues, unauthorized access, Injection flaws etc. Security analysis and review is done for all new features especially for those where there is reasonable change to any existing system</p>

<p>There are separate environments for development, testing and production. Production data is never used for testing.</p>

<h2>Administrative policies</h2>

<p>Customers - By using Quipmate, our customer agree on Terms and Conditions mentioned policy unless there is a separate agreement paper signed.</p>

<p>Employees - All employees are required to sign a non-disclosure agreement, privacy policy and security policy documents at time of hire. Potential new hire candidates are screened to minimize chances of any internal breach</p>

<p>Partners - Confidentiality and non-disclosure agreements are required from business partners. Production database is never shared with partners.</p>

<h2>General</h2>

<p>We follow generally accepted industry standards to protect personal information submitted to us, both during transmission and once we receive it. No method of transmission over the Internet, or method of electronic storage, is 100% secure, however. Therefore, while we strive to use commercially acceptable means to protect your personal information, we cannot guarantee its absolute security. We cannot guarantee the security of our database, nor can we guarantee that information you supply won&#39t be intercepted while being transmitted to us over the Internet, and any information you transmit to the Company you do at your own risk. We recommend that you not disclose your password to anyone.</p>

<p>If you have any questions about security on our Web site, you can contact us at <a href="mailto:support@quipmate.com">support@quipmate.com</a>.</p>
